# adds two numbers
def add(x, y):
    return x + y

# subtracts two numbers
def subtract(x, y):
    return x - y

# multiplies two numbers
def multiply(x, y):
    return x * y

# divides two numbers
def divide(x, y):
    return x / y 

# returns the modulus of two numbers
def mod(x, y):
    return x % y

# returns the exponent x^y
def exp(x, y):
    return x ** y